// MATEMÁTICA Y NOTACIÓN DE TRANSPOSICIÓN
const ESCALA_NOTAS = ["C", "C#", "D", "D#", "E", "F", "F#", "G", "G#", "A", "A#", "B"];
const ESCALA_FLATS = ["C", "Db", "D", "Eb", "E", "F", "Gb", "G", "Ab", "A", "Bb", "B"];

function calcularNotaTono(tonoOriginal, semitonos) {
  let idx = ESCALA_NOTAS.indexOf(tonoOriginal);
  if (idx === -1) idx = ESCALA_FLATS.indexOf(tonoOriginal);
  if (idx === -1) return tonoOriginal;

  let nuevoIdx = (idx + semitonos) % 12;
  if (nuevoIdx < 0) nuevoIdx += 12;

  return ESCALA_NOTAS[nuevoIdx];
}

function transponerAcorde(acorde, semitonos) {
  if (semitonos === 0) return acorde;

  return acorde.replace(/[A-G][b#]?/g, (notaBase) => {
    let idx = ESCALA_NOTAS.indexOf(notaBase);
    if (idx === -1) idx = ESCALA_FLATS.indexOf(notaBase);
    if (idx === -1) return notaBase;

    let nuevoIdx = (idx + semitonos) % 12;
    if (nuevoIdx < 0) nuevoIdx += 12;

    return ESCALA_NOTAS[nuevoIdx];
  });
}

function transponerTextoCancion(texto, semitonos) {
  if (semitonos === 0) return texto;

  const lines = texto.split('\n');
  return lines.map(line => {
    if (esLineaAcordes(line)) {
      return line.replace(/([A-G][b#]?(?:m|maj|min|dim|aug|add|[0-9])*(?:\/[A-G][b#]?)?)/g, (match) => {
        return transponerAcorde(match, semitonos);
      });
    }
    return line;
  }).join('\n');
}

function esLineaAcordes(line) {
  const chordRegex = /^[A-G][b#]?(m|maj|min|dim|aug|add|[0-9])?(\/[A-G][b#]?)?(\s+[A-G][b#]?(m|maj|min|dim|aug|add|[0-9])?(\/[A-G][b#]?)?)*\s*$/;
  return chordRegex.test(line.trim()) && line.trim().length > 0;
}